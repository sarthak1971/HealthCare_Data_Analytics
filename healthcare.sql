-- Patient Segmentation

-- Calculate recency, frequency, and monetary value for each patient
SELECT p.patient_id, p.patient_name,
DATEDIFF(CURRENT_DATE, MAX(a.appointment_date)) AS 'recency_days',
COUNT(DISTINCT a.appointment_id) AS 'Frequency',
SUM(t.treatment_cost) AS 'Monetary_Value' 	
FROM patients p 
JOIN appointments a ON p.patient_id = a.patient_id
JOIN treatments t ON a.appointment_id = t.appointment_id 
WHERE a.status = 'completed'
GROUP BY p.patient_id, p.patient_name
ORDER BY monetary_value DESC;

-- Assign RFM scores using NTILE
WITH Raw_RFM AS (
SELECT p.patient_id, p.patient_name,
DATEDIFF(CURRENT_DATE, MAX(a.appointment_date)) AS 'recency_days',
COUNT(DISTINCT a.appointment_id) AS 'Frequency',
SUM(t.treatment_cost) AS 'Monetary_Value' 	
FROM patients p 
JOIN appointments a ON p.patient_id = a.patient_id
JOIN treatments t ON a.appointment_id = t.appointment_id 
WHERE a.status = 'completed'
GROUP BY p.patient_id, p.patient_name
ORDER BY monetary_value DESC
)
SELECT patient_id, patient_name,
NTILE(5) OVER(ORDER BY Recency_days DESC) AS 'R_score',
NTILE(5) OVER(ORDER BY Frequency ASC) AS 'F_score',
NTILE(5) OVER(ORDER BY Monetary_Value ASC) AS 'M_score'
FROM Raw_RFM;

