-- Doctor Performance

-- Calculate revenue generated by each doctor
SELECT d.doctor_id, d.doctor_name, SUM(t.treatment_cost) AS 'Revenue'
FROM doctors d 
JOIN appointments a 
ON d.doctor_id = a.doctor_id
JOIN treatments t 
ON a.appointment_id = t.appointment_id 
WHERE a.status = 'completed'
GROUP BY d.doctor_id, d.doctor_name
ORDER BY SUM(t.treatment_cost);

-- Find average rating per doctor

SELECT d.doctor_id, d.doctor_name, AVG(r.rating) AS 'Average_Rating'
FROM doctors d 
JOIN appointments a 
ON d.doctor_id = a.doctor_id 
JOIN reviews r 
ON a.appointment_id = r.appointment_id
WHERE a.status = 'completed'
GROUP BY d.doctor_id, d.doctor_name;

-- Rank doctors within each hospital (review this again)
SELECT d.doctor_id, d.doctor_name, h.hospital_name, AVG(r.rating) AS 'Average_Rating'
FROM hospitals h 
JOIN doctors d 
ON h.hospital_id = d.hospital_id
JOIN appointments a 
ON d.doctor_id = a.doctor_id 
JOIN reviews r 
ON a.appointment_id = r.appointment_id
WHERE a.status = 'completed'
GROUP BY d.doctor_id, d.doctor_name, h.hospital_name
ORDER BY h.hospital_name, Average_Rating;

