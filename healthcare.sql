USE healthcare_analytics;

-- Revenue Analysis

-- Calculate total revenue by treatment type

SELECT t.treatment_type, SUM(t.treatment_cost) AS "Total_Revenue"
FROM treatments t 
JOIN appointments a 
ON a.appointment_id = t.appointment_id
WHERE a.status = "completed"
GROUP BY t.treatment_type;

-- Find total number of treatments per hospital
SELECT
    h.hospital_name,
    COUNT(t.treatment_id) AS total_treatments
FROM treatments t
INNER JOIN appointments a ON t.appointment_id = a.appointment_id
INNER JOIN doctors d      ON a.doctor_id      = d.doctor_id
INNER JOIN hospitals h    ON d.hospital_id    = h.hospital_id
WHERE a.status = 'completed'
GROUP BY h.hospital_id, h.hospital_name
ORDER BY total_treatments DESC;

-- Identify top 5 treatments by revenue
SELECT t.treatment_id, t.treatment_type, t.treatment_cost
FROM treatments t 
JOIN appointments a
ON t.appointment_id = a.appointment_id
WHERE a.status = 'completed'
LIMIT 5;

-- Find average treatment cost per specialization
SELECT d.specialization, AVG(treatment_cost) AS 'Average Treatment Cost'
FROM doctors d
JOIN appointments a 
ON d.doctor_id = a.doctor_id
JOIN treatments t 
ON a.appointment_id = t.appointment_id
WHERE a.status = 'completed'
GROUP BY d.specialization;

